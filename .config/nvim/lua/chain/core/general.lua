local set = vim.opt --for ease of use

-- line numbers
set.relativenumber = true
set.number = true

-- tabbing and indenting
set.tabstop = 2
set.shiftwidth = 2
set.expandtab = true
set.autoindent = true

-- line wrapping
set.wrap = false

-- search settings
set.ignorecase = true
set.smartcase = true

-- cursorline
set.cursorline = true

-- appearance
set.termguicolors = true
set.background = "dark"
set.signcolumn = "yes"

-- splits
set.splitright = true
set.splitbelow = true
