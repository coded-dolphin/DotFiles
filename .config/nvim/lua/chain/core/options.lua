local set = vim.opt --ease of use

--line numbers
set.number = true

-- tabbing and indenting
set.tabstop = 2
set.shiftwidth = 2
set.expandtab = true
set.autoindent = true

--line warpping
set.wrap = false

-- search settings
set.ignorecase = true
set.smartcase = true

-- cursorline
set.cursorline = true
set.winblend = 0
set.wildoptions = 'pum'
set.pumblend = 5
set.background = 'dark'

-- appearance
set.termguicolors = true
set.signcolumn = "yes"

-- splits
set.splitright = true
set.splitbelow = true
